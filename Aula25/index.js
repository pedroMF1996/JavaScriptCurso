const numero = 10;

if (numero >= 0 && numero <= 5) {
    console.log("Sim o número é meior ou igual a 0 e menor ou igual a 5.");
} else if (numero >= 6 && numero <= 8) {
    console.log("Sim o número é meior ou igual a 6 e menor ou igual a 8.");
} else if (numero >= 9 && numero <= 11) {
    console.log("Sim o número é meior ou igual a 9 e menor ou igual a 11.");
} else {
    console.log("O número não está entre 0 e 11.");
}